function Americano() {
    let gumarimutq = +prompt('Մուտքագրեք Ձեր գումարը: Սուրճի արժեքն է՝ 400 դրամ');
    if(gumarimutq >= '400'){
        gumarimutq = gumarimutq - 400;
        alert("Շնորհակալություն գնումների համար," + "Վերցրեք Ձեր մանրը" + " " + gumarimutq + " "  + "դրամ");
             }else{
                 alert('Սխալ գումարի չափ');
             }
}
